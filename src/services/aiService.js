const fetchAI = async (body, options = {}) => {
  const { url, headers } = getApiConfig(body.model);
  return fetch(url, {
    method: 'POST',
    headers: { ...headers, ...options.headers },
    signal: options.signal,
    body: JSON.stringify(body)
  });
};

const parseAIResponse = (content) => {
  let ai = {};
  try {
    let rawContent = (content || '').trim();
    if (rawContent.startsWith('```')) {
      rawContent = rawContent.replace(/^```[a-zA-Z]*\s*/, '').replace(/\s*```$/, '').trim();
    }
    ai = JSON.parse(rawContent);
  } catch (err) {
    console.error('Failed to parse AI JSON response:', err);
    ai = {};
  }
  return ai;
};

const gcd = (a, b) => b ? gcd(b, a % b) : a;

const simplifyRatio = (w, h) => {
  let multiplier = 1;
  while (!Number.isInteger(w * multiplier) || !Number.isInteger(h * multiplier)) {
    multiplier *= 10;
    if (multiplier > 1000) break;
  }
  let width = Math.round(w * multiplier);
  let height = Math.round(h * multiplier);
  const divisor = gcd(width, height);
  width /= divisor;
  height /= divisor;

  if (width > 100 || height > 100) {
    const factor = Math.max(width, height) / 16;
    width = Math.round(width / factor);
    height = Math.round(height / factor);
  }
  return `${width}:${height}`;
};

const parseArFromDimensi = (dimensi) => {
  if (!dimensi) return null;
  const match = dimensi.match(/(\d+(?:\.\d+)?)\s*[x×]\s*(\d+(?:\.\d+)?)/i);
  if (match) {
    const w = parseFloat(match[1]);
    const h = parseFloat(match[2]);
    if (w > 0 && h > 0) {
      const ratioStr = simplifyRatio(w, h);
      return { w, h, ratioStr, ratio: w / h };
    }
  }
  return null;
};

const getArRatio = (dimensi, formatMedia, orientasi, mjVersion) => {
  // 1. Try parsing from custom dimensions
  const parsed = parseArFromDimensi(dimensi);
  if (parsed) {
    if (mjVersion === 'dalle') {
      if (parsed.ratio > 1.2) return '16:9';
      if (parsed.ratio < 0.8) return '9:16';
      return '1:1';
    }
    return parsed.ratioStr;
  }

  // 2. Try mapping from format media
  if (formatMedia) {
    const formatArMap = {
      'Spanduk / Banner Horizontal': { mj: '3:1', dalle: '16:9' },
      'Poster / Flyer / Brosur': { mj: '2:3', dalle: '9:16' },
      'Brosur Lipat Tiga (Tri-fold)': { mj: '3:1', dalle: '16:9' },
      'Roll-up / X-Banner': { mj: '1:2', dalle: '9:16' },
      'Instagram Post / Social Media Feed': { mj: '1:1', dalle: '1:1' },
      'Billboard / Baliho Raksasa': { mj: '3:1', dalle: '16:9' },
      'Billboard Vertical Megatron': { mj: '9:16', dalle: '9:16' },
      'Web Banner Header 1200x630': { mj: '1.91:1', dalle: '16:9' },
      'Flyer Promosi A5': { mj: '2:3', dalle: '9:16' },
      'Kemasan Produk / Box Layout': { mj: '4:3', dalle: '1:1' },
      'Kartu Nama Bisnis Premium': { mj: '3.5:2', dalle: '16:9' }
    };
    const matched = formatArMap[formatMedia];
    if (matched) {
      return mjVersion === 'dalle' ? matched.dalle : matched.mj;
    }
  }

  // 3. Fallback to orientation
  const fallbackAr = { Landscape: '16:9', Portrait: '9:16', Square: '1:1' };
  return fallbackAr[orientasi] || '16:9';
};

const getPromptArText = (dimensi, formatMedia, orientasi, mjVersion, arRatio) => {
  if (mjVersion === 'dalle') {
    if (orientasi === 'Portrait') return 'graphic layout stretching vertically from the absolute top edge to the absolute bottom edge of the canvas';
    if (orientasi === 'Square') return 'graphic layout filling the entire canvas area edge-to-edge';
    return 'graphic layout stretching horizontally from the absolute left edge to the absolute right edge of the canvas';
  }
  const parsed = parseArFromDimensi(dimensi);
  const dimText = dimensi ? ` (dimensions: ${dimensi.trim()})` : '';
  if (parsed) {
    return `${parsed.ratioStr} aspect ratio${dimText}`;
  }
  return `${arRatio} aspect ratio${dimText}`;
};

const deduplicatePrompt = (prompt) => {
  if (!prompt) return '';
  const parts = prompt.split(/[,.]+/);
  const seen = new Set();
  const uniqueParts = [];
  
  for (let part of parts) {
    const trimmed = part.trim().replace(/\s+/g, ' ');
    if (!trimmed) continue;
    
    // Ignore duplicates of generic instructions
    const lower = trimmed.toLowerCase();
    if (!seen.has(lower)) {
      seen.add(lower);
      uniqueParts.push(trimmed);
    }
  }
  
  let result = '';
  for (let i = 0; i < uniqueParts.length; i++) {
    const part = uniqueParts[i];
    if (part.startsWith('--')) {
      result += ' ' + part;
    } else {
      if (result && !result.endsWith(' ')) {
        result += ', ';
      }
      result += part;
    }
  }
  return result.replace(/\s+/g, ' ').trim();
};

